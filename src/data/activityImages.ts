// Transform images by category for easy filtering
export const getImagesByCategory = (category: string) => {
  if (category === 'all') return sampleActivityImages;
  return sampleActivityImages.filter(img => img.category === category);
};

// Get featured images
export const getFeaturedImages = () => {
  return sampleActivityImages.filter(img => img.featured);
};

// Get recent images (last 30 days)
export const getRecentImages = () => {
  const thirtyDaysAgo = new Date();
  thirtyDaysAgo.setDate(thirtyDaysAgo.getDate() - 30);
  
  return sampleActivityImages.filter(img => {
    const imgDate = new Date(img.date);
    return imgDate >= thirtyDaysAgo;
  });
};